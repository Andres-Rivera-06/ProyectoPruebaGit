    public void agregar(){
        // Crear una instancia de la clase Producto
        Producto productoDB = new Producto(0, "", "", 0, 0, 0, 0, 0, 0, 0, "", "", ""); // Usamos un constructor vac√≠o
        /*Producto productoPrueba = new Producto(10, " das", "asd ", 10, 1, 10, 10, 10, 10, 10, " asd", "ad ", "ad ");*/
        // Obtener la lista de productos
        List<Producto> productos = productoDB.obtenerTodosLosProductos();
        /*productos.add(productoPrueba);*/
        for (Producto producto : productos) {
            dmt.addRow(new Object[]{
                producto.idproducto,
                producto.nombre,
                producto.unidadMedida,
                producto.cantidadProducto,
                producto.precioUnitario,
                producto.rentabilidad,
                producto.precioVenta,
                producto.porcentajeIva,
                producto.iva,
                producto.precioFinal,
                producto.nombreProveedor,
                producto.fechaIngreso,
                producto.fechaVencimiento
            });
        }
    }

