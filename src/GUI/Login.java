    private void IngresarbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarbtActionPerformed
        String usuario = Usuariotxt.getText();
        String contrasena = contratxt.getText();
        int codigoUsuario = Integer.parseInt(Usuariotxt.getText());
        if(evt.getSource() == Ingresarbt){
            consulta = new UsuariosLogin();
            if(consulta.validarUsuario(usuario, contrasena)){
                if(codigoUsuario > 1000 && codigoUsuario < 2000){
                Admin ventanaAdmin = new Admin();
                ventanaAdmin.setVisible(true);
                ventanaAdmin.setLocationRelativeTo(null);
                ventanaAdmin.setResizable(false);
                this.setVisible(false);
            }else{
                PosVendedor ventanaVendedor = new PosVendedor();
                ventanaVendedor.setVisible(true);
                ventanaVendedor.setLocationRelativeTo(null);
                ventanaVendedor.setResizable(false);
                this.setVisible(false);
                }
        }else{
            JOptionPane.showMessageDialog(this, "Usuario o contrase침a incorrectos.");
            }
        }
    }//GEN-LAST:event_IngresarbtActionPerformed

