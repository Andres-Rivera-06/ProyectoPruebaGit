    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        if (evt.getSource() == modificar) {
        double codigoproducto = Double.parseDouble(txtCodigoProducto.getText());
        String nombreProducto = txtNombreProducto.getText();
        String unidadMedida = txtUnidadMedida.getText();
        double precioUnitario = Double.parseDouble(txtPrecioUnitario.getText());
        double rentabilidad = Double.parseDouble(txtRentabilidad.getText());
        double precioVenta = Double.parseDouble(txtPrecioVenta.getText());
        double porcentajeIva = Double.parseDouble(txtPorcentajeIva.getText());
        double precioFinal = Double.parseDouble(txtPrecioFinal.getText());
        int cantidad = Integer.parseInt(txtCantidad.getText());
        String proveedor = txtProveedor.getText();
        String fechaIngreso = txtFechaIngreso.getText();
        String fechaVencimiento = txtFechaVencimiento.getText();
            
            // Llamar al método para modificar el producto
            boolean modificado = modificarproducto.ModificarProducto(codigoproducto, nombreProducto, precioUnitario, cantidad, porcentajeIva, unidadMedida, proveedor, fechaIngreso, fechaVencimiento, precioVenta, rentabilidad, precioFinal);

            if (modificado) {
                txtCodigoProducto.setEditable(true);
                txtNombreProducto.setEditable(false);
                txtUnidadMedida.setEditable(false);
                txtPrecioUnitario.setEditable(false);
                txtRentabilidad.setEditable(false);
                txtPrecioVenta.setEditable(false);
                txtPorcentajeIva.setEditable(false);
                txtPrecioFinal.setEditable(false);
                txtCantidad.setEditable(false);
                txtProveedor.setEditable(false);
                txtFechaIngreso.setEditable(false);
                txtFechaVencimiento.setEditable(false);
                
                
                JOptionPane.showMessageDialog(this, "Producto modificado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                // Limpiar los campos de texto
                txtCodigoProducto.setText("");
                txtNombreProducto.setText("");
                txtUnidadMedida.setText("");
                txtPrecioUnitario.setText("");
                txtRentabilidad.setText("");
                txtPrecioVenta.setText("");
                txtPorcentajeIva.setText("");
                txtPrecioFinal.setText("");
                txtCantidad.setText("");
                txtProveedor.setText("");
                txtFechaIngreso.setText("");
                txtFechaVencimiento.setText("");
            } else {
                // Mostrar mensaje de error si no se pudo eliminar
                JOptionPane.showMessageDialog(this, "No se encontró el producto o no se pudo modificar.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_modificarActionPerformed

    
