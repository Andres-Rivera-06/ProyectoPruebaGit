    private void consultarBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarBtActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == consultarBt) {
            int idProducto = Integer.parseInt(txtCodigoProducto.getText());
            txtCodigoProducto.setEditable(false);
            txtNombreProducto.setEditable(true);
            txtUnidadMedida.setEditable(true);
            txtPrecioUnitario.setEditable(true);
            txtRentabilidad.setEditable(true);
            txtPrecioVenta.setEditable(false);
            txtPorcentajeIva.setEditable(true);
            txtPrecioFinal.setEditable(false);
            txtCantidad.setEditable(true);
            txtProveedor.setEditable(true);
            txtFechaIngreso.setEditable(true);
            txtFechaVencimiento.setEditable(true);
            

            // Crear una instancia de Consultar
            consultarproducto = new Producto();

            // Obtener los datos del producto
            String[] productoDatos = consultarproducto.obtenerProductoPorId(idProducto);

            if (productoDatos != null) {
                // Llenar los campos de texto con los datos del producto
                txtCodigoProducto.setText(productoDatos[0]); // Código de producto
                txtNombreProducto.setText(productoDatos[1]); // Nombre
                txtPrecioUnitario.setText(productoDatos[2]); // precioUnitario
                txtCantidad.setText(productoDatos[3]); // cantidad
                txtUnidadMedida.setText(productoDatos[5]); // unidadMedida
                txtProveedor.setText(productoDatos[6]); // proveedor
                txtFechaIngreso.setText(productoDatos[7]); // fechaingreso
                txtFechaVencimiento.setText(productoDatos[8]); // fechavencimiento
                txtPrecioFinal.setText(productoDatos[9]); // precioFinal
                txtPorcentajeIva.setText(productoDatos[10]); // porcentajeIva
                txtRentabilidad.setText(productoDatos[11]); // Rentabilidad
                txtPrecioVenta.setText(productoDatos[12]); // calculaPrecioVenta
            } else {
                // Mostrar un mensaje si no se encuentra el usuario
                JOptionPane.showMessageDialog(this, "producto no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
                // Limpiar los campos si no se encuentra el usuario
                txtCodigoProducto.setText(""); // Código de producto
                txtNombreProducto.setText(""); // Nombre
                txtUnidadMedida.setText(""); // unidadMedida
               txtPrecioUnitario.setText(""); // precioUnitario
                txtRentabilidad.setText(""); // Rentabilidad
                txtPrecioVenta.setText(""); // calculaPrecioVenta
                txtPorcentajeIva.setText(""); // porcentajeIva
                txtPrecioFinal.setText(""); // precioFinal
                txtCantidad.setText(""); // cantidad
                txtProveedor.setText(""); // proveedor
                txtFechaIngreso.setText(""); // fechaingreso
                txtFechaVencimiento.setText(""); // fechavencimiento
            }
        }
    }//GEN-LAST:event_consultarBtActionPerformed

